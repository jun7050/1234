#include<stdio.h>

int main()
{
	int price;
	printf("1000원 미만 가격:");
	scanf("%d", &price);
	int asdf = 1000 - price;
	int a = asdf / 500;
	asdf %= 500;
	int b = asdf / 100;
	asdf %= 100;
	int c = asdf / 50;
	asdf %= 50;
	int d = asdf / 10;
	asdf %= 10;

	printf("500원 %d개, 100원 %d개, 50원 %d개, 10원 %d개", a, b, c, d);
	return 0;
}